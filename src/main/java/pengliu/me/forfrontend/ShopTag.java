package pengliu.me.forfrontend;

public class ShopTag {
    private String name;

    public ShopTag(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return this.name;
    }
}
